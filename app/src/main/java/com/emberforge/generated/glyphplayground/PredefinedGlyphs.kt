package com.emberforge.generated.glyphplayground

/**
 * Ready-made glyphs the user can drop straight onto the 25x25 matrix without
 * drawing them by hand.
 *
 * Each glyph is authored as 25 rows of 25 characters where `#` marks a lit LED
 * and any other character is off. Rows are parsed into the same flat LED-index
 * [Set] the editor and [GlyphController] already use, and anything that would
 * fall outside the physical circular boundary is dropped via
 * [GlyphLayout.VALID_INDICES].
 */
data class PredefinedGlyph(val name: String, val activeLeds: Set<Int>)

object PredefinedGlyphs {

    val ALL: List<PredefinedGlyph> = listOf(
        PredefinedGlyph("Heart", fromRows(HEART)),
        PredefinedGlyph("Smile", fromRows(SMILE)),
        PredefinedGlyph("Thumbs Up", fromRows(THUMBS_UP)),
        PredefinedGlyph("Yes", fromRows(YES)),
        PredefinedGlyph("No", fromRows(NO)),
        PredefinedGlyph("Circle", fromRows(CIRCLE)),
        PredefinedGlyph("Triangle", fromRows(TRIANGLE)),
        PredefinedGlyph("Rectangle", fromRows(RECTANGLE)),
        PredefinedGlyph("Star", fromRows(STAR)),
        PredefinedGlyph("Arrow Up", fromRows(ARROW_UP)),
        PredefinedGlyph("Arrow Down", fromRows(ARROW_DOWN)),
        PredefinedGlyph("Arrow Left", fromRows(ARROW_LEFT)),
        PredefinedGlyph("Arrow Right", fromRows(ARROW_RIGHT))
    )

    private fun fromRows(rows: Array<String>): Set<Int> = buildSet {
        for (row in 0 until GlyphLayout.GRID_SIZE) {
            val line = rows.getOrNull(row) ?: continue
            for (col in 0 until GlyphLayout.GRID_SIZE) {
                if (col < line.length && line[col] == '#') {
                    val index = GlyphLayout.indexOf(row, col)
                    if (GlyphLayout.isInsideCircle(index)) add(index)
                }
            }
        }
    }
}

